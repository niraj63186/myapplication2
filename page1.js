function isEven(number){
    if(number%2==0)
        console.log('number is even')
    else
        console.log('not even')
}

isEven(10)
isEven(11)

function isOdd(number){
    if(number%2==0)
        console.log('number is not odd')
    else
        console.log('odd number')
}

isOdd(13)
isOdd(12)

